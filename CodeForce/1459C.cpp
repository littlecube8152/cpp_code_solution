#include <bits/stdc++.h>
#define ll long long
using namespace std;

unsigned ll n, m, a[200005],result[200005], tmp, b, gcd, diff_gcd;

unsigned ll _gcd(ll a, ll b){
	while(b!=0){
  		a %= b;
		swap(a,b);
	}
	return a;
}

int main(){

	cin >> n >> m;
	if(n == 1){
		cin >> a[0];
		for(int i = 0; i < m; i++){
			cin >> b;
			cout << a[0] + b << "  ";
		}
		return 0;
	}
	for(int i = 0; i < n; i++){
		cin >> a[i];
	}
	gcd = a[0];
	for(int j = 1; j < n; j++){
		gcd = _gcd(a[j], gcd);

  	}
  	diff_gcd = abs(a[1] - a[0]);
  	for(int j = 1; j < n; j++){
		diff_gcd = _gcd(abs(a[j] - a[j-1]), diff_gcd);
  	}
  	diff_gcd /= gcd;

	for(int i = 0; i < diff_gcd; i++){
		tmp = a[0] + i;
		for(int j = 1; j < n; j++){
			if(tmp == 1)break;
				tmp = _gcd(a[j] + i, tmp);

  		}
		result[i] = tmp;
	}
	for(int i = 0; i < m; i++){
		cin >> b;
 		tmp = result[b % diff_gcd];
		cout << tmp << "  ";
	}
	return 0;
}



