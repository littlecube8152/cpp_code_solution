struct custom_hash
{
    static uint64_t splitmix64(uint64_t x)
    {
        // http://xorshift.di.unimi.it/splitmix64.c
        x += 0x9e3779b97f4a7c15;
        x = (x ^ (x >> 30)) * 0xbf58476d1ce4e5b9;
        x = (x ^ (x >> 27)) * 0x94d049bb133111eb;
        return x ^ (x >> 31);
    }

    size_t operator()(uint64_t x) const
    {
        static const uint64_t FIXED_RANDOM = chrono::steady_clock::now().time_since_epoch().count();
        return splitmix64(x + FIXED_RANDOM);
    }
};

const ll mod = 998244353;
int n, m, k, deg[200005], E, O;
ll fac[200005], ans;

ll inv(ll b)
{
    ll p = mod - 2, a = 1;
    while (p)
    {
        if (p & 1)
            a = a * b % mod;
        b = b * b % mod;
        p >>= 1;
    }
    return a;
}

ll C(int n, int k)
{
    return (fac[n] * inv(fac[n - k]) % mod) * inv(fac[k]) % mod;
}

signed main()
{
    fast;
    cin >> n >> m >> k;
    fac[0] = 1;
    for (int i = 1; i <= n; i++)
        fac[i] = fac[i - 1] * i % mod;
    for (int i = 1; i <= m; i++)
    {
        int u, v;
        cin >> u >> v;
        deg[u]++, deg[v]++;
    }
    for (int i = 1; i <= n; i++)
        if (deg[i] & 1)
            O++;
        else
            E++;
    for (int i = 0; i <= O; i += 2)
        if (E >= k - i && k - i >= 0)
            ans = (ans + C(O, i) * C(E, k - i)) % mod;
    cout << ans << '\n';
}
