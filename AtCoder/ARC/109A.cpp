int a, b, x, y, ans = 1e9;

signed main()
{
    fast;
    cin >> a >> b >> x >> y;
    if (a <= b)
        ans = min(ans, (b - a) * y + x);
    else
        ans = min(ans, (a - b - 1) * y + x);
    if (a <= b)
        ans = min(ans, (2 * b - 2 * a + 1) * x);
    else
        ans = min(ans, (2 * a - 2 * b - 1) * x);
    cout << ans << '\n';
}
