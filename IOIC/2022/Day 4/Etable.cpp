#include <bits/stdc++.h>
using namespace std;
#define ll long long

ll N, t, sg[100005];

int main()
{
    cin >> N >> t;
    for (int i = 1; i <= t; i++)
        sg[i] = 0;
    for (int i = t + 1; i <= N; i++)
    {
        int mex[100005] = {};
        for (int j = 0; j <= i - (t + 1); j++)
        {
            int k = i - (t + 1) - j;
            mex[sg[j] ^ sg[k]] = 1;
        }
        for (int j = 0; j <= i - (t + 2); j++)
        {
            int k = i - (t + 2) - j;
            mex[sg[j] ^ sg[k]] = 1;
        }
        for (int j = 0; j <= 1e5; j++)
            if (mex[j] == 0)
            {
                sg[i] = j;
                break;
            }
    }
    for (int i = t + 1; i <= N; i++)
        cout << sg[i] << ",";
}
